nota1 = float(input('Digite a 1º nota do aluno:'))#recebe o valor da nota 1
nota2 = float(input('Digite a 2º nota do aluno:'))#recebe o valor da nota 2
nota3 = float(input('Digite a 3º nota do aluno:'))#recebe o valor da nota 3
nota4 = float(input('Digite a 4º nota do aluno:'))#recebe o valor da nota 4


mediafinal = (nota1+nota2+nota3+nota4)/4#calcula a media final
print('A média final do aluno é: ' + str(mediafinal))#exibe a media final para o usuario